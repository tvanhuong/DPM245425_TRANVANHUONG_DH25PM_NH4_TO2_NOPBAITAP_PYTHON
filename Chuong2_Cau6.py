#Trình bày ý nghĩa toán tử /, //, %, **, and, or, is
''' 
/ : Thực hiện phép chia và luôn trả về kết quả dạng số thực, dù chia hết hay không.
// : Chia hai số và lấy phần nguyên (loại bỏ phần thập phân).
% : Trả về phần dư sau khi chia hai số.
** : Tính lũy thừa (mũ) của một số.
and : Trả về True nếu cả hai biểu thức đều đúng, ngược lại trả về False.
or : Trả về True nếu ít nhất một biểu thức đúng.
is : Kiểm tra xem hai biến có trỏ đến cùng một đối tượng trong bộ nhớ hay không.
'''